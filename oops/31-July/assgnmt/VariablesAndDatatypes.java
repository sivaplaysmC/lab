public class VariablesAndDatatypes {
  public static void main(String[] args) {
    int i = 10;
    float f = 4.2f;
    double d = 4.2;
    char c = 'a';
    String s = "asdf";

    System.out.println(i);
    System.out.println(f);
    System.out.println(d);
    System.out.println(c);
    System.out.println(s);

  }
}
